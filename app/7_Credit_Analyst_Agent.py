def _top_shap_features(shap_values, idx: int, n: int = 5) -> list[tuple[str, float]]:
    return shap_top_features(shap_values, idx, n)


def _shap_delta(shap_values, idx_a: int, idx_b: int, n: int = 5) -> list[tuple[str, float, float]]:
    """Return top-n features by |delta SHAP| between two applicants."""
    sv_a = shap_values[idx_a]
    sv_b = shap_values[idx_b]
    vals_a = sv_a.values.mean(axis=1) if sv_a.values.ndim == 2 else sv_a.values
    vals_b = sv_b.values.mean(axis=1) if sv_b.values.ndim == 2 else sv_b.values
    delta  = vals_b - vals_a
    top_idx = np.argsort(np.abs(delta))[::-1][:n]
    return [(sv_a.feature_names[i], float(vals_a[i]), float(vals_b[i])) for i in top_idx]


# ── llama.cpp subprocess backend ─────────────────────────────────────────────

def _find_llama_binary() -> str | None:
    """Return path to llama-cli binary, or None if not found."""
    for candidate in _LLAMA_BIN_CANDIDATES:
        if shutil.which(candidate):
            return candidate
        if Path(candidate).exists():
            return candidate
    return None


def _run_llama(prompt: str, model_path: str, n_predict: int = 300, temperature: float = 0.3) -> str:
    """
    Run llama-cli with the given prompt and return the generated text.

    Uses --no-display-prompt so only the completion is returned.
    Runs CPU-only (no GPU flags) for maximum portability.
    Context window set to 2048 — sufficient for credit explanation prompts.
    """
    binary = _find_llama_binary()
    if binary is None:
        return ""

    cmd = [
        binary,
        "--model",       model_path,
        "--prompt",      prompt,
        "--n-predict",   str(n_predict),
        "--temp",        str(temperature),
        "--ctx-size",    "2048",
        "--threads",     str(max(1, os.cpu_count() // 2)),
        "--no-display-prompt",
        "--log-disable",
    ]

    try:
        result = subprocess.run(
            cmd,
            capture_output=True,
            text=True,
            timeout=120,   # 2-minute hard limit
        )
        output = result.stdout.strip()
        # Strip any residual prompt echo that some versions include
        if prompt.strip() in output:
            output = output.replace(prompt.strip(), "").strip()
        return output
    except subprocess.TimeoutExpired:
        return "[Generation timed out after 120 seconds. Try a smaller model or reduce n_predict.]"
    except FileNotFoundError:
        return ""
    except Exception as e:
        return f"[llama.cpp error: {e}]"


# ── Prompt templates ──────────────────────────────────────────────────────────

def _build_explain_prompt(
    idx: int,
    pred_class: str,
    confidence: float,
    top_features: list[tuple[str, float]],
    label_map: dict,
) -> str:
    feat_lines = "\n".join(
        f"  {i+1}. {name}: {val:+.4f} ({'increases' if val > 0 else 'decreases'} approval chance)"
        for i, (name, val) in enumerate(top_features)
    )
    tier_desc = TIER_DESCRIPTIONS.get(pred_class, "")
    return textwrap.dedent(f"""
        You are a credit analyst AI for an Indian bank. Explain the following credit decision
        in plain English. Be concise (3-4 sentences). End with one actionable recommendation.

        Applicant index: {idx}
        Predicted credit tier: {pred_class} — {tier_desc}
        Confidence: {confidence:.1%}

        Top SHAP feature contributions (positive = supports approval):
        {feat_lines}

        Format your response as:
        DECISION: [one sentence stating the outcome]
        REASONS: [2-3 bullet points with the key factors]
        RECOMMENDATION: [one actionable sentence for the applicant]
    """).strip()


def _build_compare_prompt(
    idx_a: int, pred_a: str, conf_a: float,
    idx_b: int, pred_b: str, conf_b: float,
    delta_features: list[tuple[str, float, float]],
) -> str:
    delta_lines = "\n".join(
        f"  {name}: applicant {idx_a}={va:+.4f}, applicant {idx_b}={vb:+.4f} (delta={vb-va:+.4f})"
        for name, va, vb in delta_features
    )
    return textwrap.dedent(f"""
        You are a credit analyst AI. Compare two loan applicants and explain the key differences.
        Be concise (4-5 sentences total).

        Applicant {idx_a}: tier {pred_a} ({conf_a:.1%} confidence)
        Applicant {idx_b}: tier {pred_b} ({conf_b:.1%} confidence)

        Largest SHAP differences between the two applicants:
        {delta_lines}

        Explain why applicant {idx_b} received a {'better' if pred_b < pred_a else 'worse'} tier
        than applicant {idx_a}, referencing the specific features above.
    """).strip()


def _build_global_prompt(top_global: list[tuple[str, float]]) -> str:
    feat_lines = "\n".join(
        f"  {i+1}. {name} (mean |SHAP| = {val:.4f})"
        for i, (name, val) in enumerate(top_global)
    )
    return textwrap.dedent(f"""
        You are a credit analyst AI for an Indian bank. Explain what drives credit approval
        decisions in this model, based on the global SHAP feature importance below.
        Write 3-4 sentences in plain English for a non-technical audience.

        Top 10 most important features (by mean absolute SHAP value):
        {feat_lines}

        Focus on what these features mean in a credit context and why they matter.
    """).strip()


def _build_fairness_prompt(attr: str, dpd: float, eod: float, sr_dict: dict) -> str:
    sr_lines = "\n".join(
        f"  {group}: {rate:.1%} approval rate"
        for group, rate in sorted(sr_dict.items(), key=lambda x: -x[1])
    )
    return textwrap.dedent(f"""
        You are a credit analyst AI explaining fairness metrics to a bank compliance officer.
        Be concise and professional (3-4 sentences).

        Fairness analysis for attribute: {attr}
        Demographic Parity Difference: {dpd:.4f}
        Equalized Odds Difference: {eod:.4f}

        Approval rates by group:
        {sr_lines}

        Thresholds: <0.05 acceptable, 0.05-0.10 monitor, >0.10 action required.
        Explain what these numbers mean and whether action is needed.
    """).strip()


def _build_performance_prompt(metrics: dict) -> str:
    lr  = metrics.get("lr", {})
    ebm = metrics.get("ebm", {})
    return textwrap.dedent(f"""
        You are a credit analyst AI explaining model performance to a bank manager.
        Write 3-4 sentences in plain English. Avoid technical jargon.

        Model performance on {metrics.get('n_test', 'N/A'):,} test applicants:

        Logistic Regression:
          - Macro F1: {lr.get('f1_macro', 0):.3f}
          - AUC (OvR): {lr.get('auc_ovr', 0):.3f}

        Explainable Boosting Machine (EBM):
          - Macro F1: {ebm.get('f1_macro', 0):.3f}
          - AUC (OvR): {ebm.get('auc_ovr', 0):.3f}

        Explain what these numbers mean for the bank's credit decisions.
    """).strip()


# ── Fallback template responses (no SLM required) ────────────────────────────

def _fallback_explain(idx, pred_class, confidence, top_features) -> str:
    tier_desc = TIER_DESCRIPTIONS.get(pred_class, "")
    top3 = top_features[:3]
    reasons = "\n".join(
        f"  • **{name}**: SHAP contribution {val:+.4f} "
        f"({'positive' if val > 0 else 'negative'} impact on approval)"
        for name, val in top3
    )
    rec_feature = top3[0][0] if top3 else "credit utilization"
    return (
        f"**DECISION:** Applicant {idx} was classified as **{pred_class}** "
        f"({tier_desc}) with {confidence:.1%} confidence.\n\n"
        f"**REASONS:**\n{reasons}\n\n"
        f"**RECOMMENDATION:** Focus on improving `{rec_feature}` — "
        f"it has the largest impact on this applicant's credit tier."
    )


def _fallback_compare(idx_a, pred_a, conf_a, idx_b, pred_b, conf_b, delta_features) -> str:
    top = delta_features[:3]
    lines = "\n".join(
        f"  • **{name}**: applicant {idx_a} = {va:+.4f}, applicant {idx_b} = {vb:+.4f}"
        for name, va, vb in top
    )
    return (
        f"**Applicant {idx_a}**: {pred_a} ({conf_a:.1%} confidence)\n"
        f"**Applicant {idx_b}**: {pred_b} ({conf_b:.1%} confidence)\n\n"
        f"**Key differences:**\n{lines}"
    )


def _fallback_global(top_global) -> str:
    lines = "\n".join(
        f"  {i+1}. **{name}** (mean |SHAP| = {val:.4f})"
        for i, (name, val) in enumerate(top_global[:10])
    )
    return f"**Top features driving credit decisions:**\n{lines}"


def _fallback_fairness(attr, dpd, eod, sr_dict) -> str:
    lines = "\n".join(
        f"  • **{g}**: {r:.1%}" for g, r in sorted(sr_dict.items(), key=lambda x: -x[1])
    )
    dpd_status = "✅ Acceptable" if dpd < 0.05 else ("⚠️ Monitor" if dpd < 0.10 else "🚨 Action Required")
    eod_status = "✅ Acceptable" if eod < 0.05 else ("⚠️ Monitor" if eod < 0.10 else "🚨 Action Required")
    return (
        f"**Fairness analysis for {attr}:**\n\n"
        f"Approval rates by group:\n{lines}\n\n"
        f"**Demographic Parity Difference:** {dpd:.4f} — {dpd_status}\n"
        f"**Equalized Odds Difference:** {eod:.4f} — {eod_status}"
    )


def _fallback_performance(metrics) -> str:
    lr  = metrics.get("lr", {})
    ebm = metrics.get("ebm", {})
    return (
        f"**Model performance on {metrics.get('n_test', 'N/A'):,} test applicants:**\n\n"
        f"| Model | Macro F1 | AUC (OvR) |\n"
        f"|-------|----------|----------|\n"
        f"| Logistic Regression | {lr.get('f1_macro', 0):.3f} | {lr.get('auc_ovr', 0):.3f} |\n"
        f"| EBM | {ebm.get('f1_macro', 0):.3f} | {ebm.get('auc_ovr', 0):.3f} |"
    )


