"""
Page 7 — Credit Analyst Agent

A chat-style interface where credit officers ask natural language questions
and receive SHAP/LIME-backed explanations generated by a local SLM via llama.cpp.

Architecture
────────────
  User query (st.chat_input)
      ↓
  Intent classifier (regex, no LLM needed for routing)
      ↓
  Tool dispatcher → computes structured context (SHAP values, fairness metrics)
      ↓
  Prompt builder → fills a tight template with real numbers
      ↓
  llama.cpp subprocess → local GGUF model (Phi-3-mini or Qwen2.5-1.5B)
      ↓
  Streamed response (st.write_stream via generator)

SLM backend
───────────
  Runtime : llama.cpp CLI binary  (llama-cli.exe on Windows, llama-cli on Linux/Mac)
  Model   : any GGUF ≤ 4B params — recommended Phi-3-mini-4k-instruct Q4_K_M (~2.2 GB)
            or Qwen2.5-1.5B-Instruct Q4_K_M (~1.0 GB) for lower RAM
  No Python build step required — the binary is a standalone executable.
  Falls back to a deterministic template response when the binary is not found.

Session state consumed
──────────────────────
  processed_df    : pd.DataFrame  — full feature matrix + target
  feature_names   : list[str]     — post-encoding column names
  label_map       : dict          — {0:'P1', 1:'P2', 2:'P3', 3:'P4'}
  lr_model / ebm_model            — trained sklearn / EBM models
  X_test, y_test                  — test split (for fairness queries)
  model_metrics                   — evaluation dict from Page 3
  external_df                     — raw external dataset (sensitive attrs)
  chat_history                    — list[dict] conversation log
"""

